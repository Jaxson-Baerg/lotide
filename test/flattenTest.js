const assert = require("chai").assert;
const _ = require("../index");

describe("#flatten", () => {
  it("returns [1, 2, 3, 11, 22, 9, 8, 7, 123, 4, 44, 22, 5, 6] for [1, 2, [[3, 11, 22, [9, 8, [7, 123]]], 4, 44], 22, 5, [[[6]]]]", () => {
    assert.deepEqual(_.flatten([1, 2, [[3, 11, 22, [9, 8, [7, 123]]], 4, 44], 22, 5, [[[6]]]]), [1, 2, 3, 11, 22, 9, 8, 7, 123, 4, 44, 22, 5, 6]);
  });
});